package projava.basic.loopsample;

public class ForSample {
    public static void main(String[] args) {
        // [main] -> [tab] -> [fori] -> [tab] -> [tab] -> [5] -> [tab] -> [sout] -> [tab]
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
        System.out.println();
        for (int i = 0; i < 3; i++) {
            System.out.println("Hello");
        }
        System.out.println();
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println();
        for (int i = 0; i < 10; i++) {
            System.out.println(i + 1);
        }
    }
}
